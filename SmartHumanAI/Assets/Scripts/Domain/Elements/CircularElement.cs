namespace Domain.Elements
{
    internal class CircularElement
    {
        public float X = 0f;
        public float Y = 0f;
        public float Radius = 0f;

        public CircularElement(float pX, float pY, float pRadius)
        {
            X = pX;
            Y = pY;
            Radius = pRadius;
        }
    }
}
